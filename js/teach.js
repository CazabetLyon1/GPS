function activatePopover(id) {
    let realId = '#' + id;
    $(realId).popover('show');
    $(realId).popover('disable');
}

function desactivatePopover(id) {
    let realId = '#' + id;
    $(realId).popover('hide');
    $(realId).popover('disable');
}

function teach() {
    activatePopover("deletePoint");
    activatePopover("movePoint");
    desactivatePopover("movePoint");
}
